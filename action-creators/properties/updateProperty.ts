import axios from "axios";
export const updateProperty = async ({
    category,
    gatedCommunity,
    propertyType,
    sba,
    plotArea,
    status,
    carpetArea,
    plotConfiguration,
    constructionStatus,
    directionFacing,
    configuration,
    furnishingType,
    availableFrom,
    postedOn,
    propertyId,
    cpName,
    propertyLimits,
    reraNo,
    propertyAge,
    projectName,
    builderName,
    waterSupply,
    launchDate,
    visitorParking,
    brochure,
    noOfOwners,
    khataInOwnersName,
    lastTaxPaid,
    ownerName,
    ownerContact,
    towerWing,
    phase,
    floor,
    totalFloor,
    unitName,
    doorFacing,
    noOfBathrooms,
    parking,
    balcony,
    flooring,
    noOfParking,
    parkingType,
    propertyAddressZone,
    city,
    state,
    propertyAddress,
    country,
    nearestLandmark,
    propertyLocation,
    pincode,
    lat,
    lng,
    snagDetails,
    repairs,
    gallery,
    amenities,
    documents,
    tenancyStatus,
    tenantType,
    securityDeposit,
    tenancyExitStatus,
    petsAllowed,
    nonVegAllowed,
    bookingPricing,
    propertyActivateStatus
}: any) => {
  const url =
    process.env.NEXT_PUBLIC_NODE_ENV === "development"
      ? process.env.NEXT_PUBLIC_INVESTAY_LINK_LOCAL
      : process.env.NEXT_PUBLIC_DATA_API_URL;

  const { data } = await axios.put(`${url}/api/property/updateProperty`, {
    category,
    gatedCommunity,
    propertyType,
    sba,
    plotArea,
    carpetArea,
    plotConfiguration,
    constructionStatus,
    directionFacing,
    configuration,
    furnishingType,
    availableFrom,
    postedOn,
    propertyId,
    cpName,
    propertyLimits,
    reraNo,
    propertyAge,
    projectName,
    builderName,
    waterSupply,
    launchDate,
    visitorParking,
    brochure,
    noOfOwners,
    khataInOwnersName,
    lastTaxPaid,
    ownerName,
    ownerContact,
    status,
    towerWing,
    phase,
    floor,
    totalFloor,
    unitName,
    doorFacing,
    noOfBathrooms,
    parking,
    balcony,
    flooring,
    noOfParking,
    parkingType,
    propertyAddressZone,
    city,
    state,
    propertyAddress,
    country,
    nearestLandmark,
    propertyLocation,
    pincode,
    lat,
    lng,
    snagDetails,
    repairs,
    gallery,
    amenities,
    documents,
    tenancyStatus,
    tenantType,
    securityDeposit,
    tenancyExitStatus,
    petsAllowed,
    nonVegAllowed,
    bookingPricing,
    propertyActivateStatus
  });
  return data;
};
